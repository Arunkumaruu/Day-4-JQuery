// Event listener for password input to check strength
document.addEventListener('DOMContentLoaded', function () {
    const passwordInput = document.getElementById('password'); 
    const confirmPasswordInput = document.getElementById('confirmPassword');
    const passwordStrengthIndicator = document.getElementById('password-strength');
    const passwordMessage = document.getElementById('password-message'); 

    if (passwordInput && passwordStrengthIndicator) {
        passwordInput.addEventListener('input', function () {
            const password = passwordInput.value;
            const strength = checkPasswordStrength(password);
            passwordStrengthIndicator.textContent = strength.message;
            passwordStrengthIndicator.style.color = strength.color;
        });
    }

    // Function to check password strength
    function checkPasswordStrength(password) {
        let strength = {
            message: 'Weak',
            color: '#ff0000' // Red
        };

        if (password.length >= 8) {
            strength.message = 'Medium';
            strength.color = 'blue'; // Blue
        }

        if (password.length >= 12 && /[A-Z]/.test(password) && /[0-9]/.test(password) && /[!@#$%^&*]/.test(password)) {
            strength.message = 'Strong';
            strength.color = '#00ff00'; // Green
        }

        return strength;
    }

    // Function to validate if passwords match
    function validatePasswords() {
        if (passwordInput.value === confirmPasswordInput.value) {
            passwordMessage.textContent = 'Passwords match';
            passwordMessage.style.color = 'green';
        } else {
            passwordMessage.textContent = 'Passwords do not match';
            passwordMessage.style.color = 'red';
        }
    }

    // Validate passwords on input
    passwordInput.addEventListener('input',checkPasswordStrength);
    confirmPasswordInput.addEventListener('input', validatePasswords);
    
});
