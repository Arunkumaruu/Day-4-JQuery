document.addEventListener('DOMContentLoaded', function() {
    const form = document.getElementById('registrationForm');
    const emailInput = document.getElementById('email');
    const phoneInput = document.getElementById('phone');
    const emailFeedback = document.getElementById('emailFeedback'); // Feedback element for email
    const phoneFeedback = document.getElementById('phoneFeedback'); // Feedback element for phone

    // Email validation function
    function validateEmail() {
        const email = emailInput.value;
        const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        
        if (!emailPattern.test(email)) {
            emailFeedback.textContent = 'Please enter a valid email address.';
            emailFeedback.style.color = 'red';
        } else {
            emailFeedback.textContent = '';
        }
    }

    // Phone number validation function
    function validatePhone() {
        const phone = phoneInput.value;
        const phonePattern = /^\d{10}$/;
        
        if (!phonePattern.test(phone)) {
            phoneFeedback.textContent = 'Phone number must be exactly 10 digits.';
            phoneFeedback.style.color = 'red';
        } else {
            phoneFeedback.textContent = '';
        }
    }

    // Event listeners for real-time validation
    emailInput.addEventListener('input', validateEmail);
    phoneInput.addEventListener('input', validatePhone);

    // Form submit validation
    form.addEventListener('submit', function(event) {
        validateEmail();
        validatePhone();
        
        if (emailFeedback.textContent || phoneFeedback.textContent) {
            event.preventDefault(); 
        }
    });
});


// Function to calculate age based on birthdate
function calculateAge() {
    const birthdate = document.getElementById('birthdate').value;
    if (!birthdate) {
        document.getElementById('age').innerText = "";
        document.getElementById('restriction').innerText = "";
        return;
    }
    const birthDate = new Date(birthdate);
    const today = new Date();
    let age = today.getFullYear() - birthDate.getFullYear();
    const monthDifference = today.getMonth() - birthDate.getMonth();
    if (monthDifference < 0 || (monthDifference === 0 && today.getDate() < birthDate.getDate())) {
        age--;
    }
    document.getElementById('age').innerText = "Your age is: " + age;

    if (birthDate > today) {
        document.getElementById('restriction').innerText = "Future date not allowed";
        document.getElementById('age').innerText = "";
    } 
   else if (age < 18) {
        document.getElementById('restriction').innerText = "You are under 18. Access is restricted.";
    } 
    else {
        document.getElementById('restriction').innerText = "";
    }
}

